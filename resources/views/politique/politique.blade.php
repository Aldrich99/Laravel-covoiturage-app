                <p>
                    L'Organisateur s'engage à&nbsp;:
                </p>

                <ul>
                    <li>Fournir à NIOUDEME les données personnelles à collecter dans le cadre des
                    transactions, ce en paramétrant son ou ses événements. L'Organisateur
                    s'engage à ce titre à ne permettre la collecte pour son propre compte que de
                    données strictement nécessaires à la réalisation des transactions et à
                    l'organisation des événements,</li>
                    <li>Documenter par écrit toute instruction concernant le traitement des données,</li>
                    <li>Respecter lui-même les obligations lui incombant relatives au règlement
                    européen sur la protection des données et garantir NIOUDEME à ce titre,</li>
                    <li>Superviser le traitement et en assumer la responsabilité.</li>
                </ul>

                <p>L'Organisateur s'engage par ailleurs à mettre en œuvre les mesures de sécurité suivantes dans son organisation interne&nbsp;:</p>

                <ul>
                    <li>Pseudonymisation et chiffrement des données sensibles à caractère personnel,</li>
                    <li>Mise en œuvre des moyens permettant de garantir la confidentialité, l'intégrité, la disponibilité et la résilience constante des systèmes et des services de traitement,
                    </li><li>Les moyens permettant de rétablir la disponibilité des données à caractère
                    personnel et l'accès à celles-ci en cas d'incidents physiques ou techniques,</li>
                    <li>Une procédure visant à tester, analyser, à évaluer régulièrement l'efficacité des
                    mesures techniques et organisationnelles pour assurer la sécurité du traitement des données.</li>
                </ul>

                <p>
                    Sur demande, l'Organisateur devra pouvoir justifier des process mis en œuvre afin de garantir la sécurité des données personnelles collectées et traitées par NIOUDEME pour le compte de l'Organisateur. NIOUDEME se réserve en outre le droit de suspendre l'exécution de tout ou partie des clauses de la présente convention en cas de doute légitime sur la capacité de l'Organisateur à se conformer à ses obligations légales et règlementaires relatives à la protection des données à caractère personnel et en cas d'utilisation abusive de ses services. NIOUDEME n'assumera aucune responsabilité quant aux violations par l'Organisateur de ses propres obligations, la responsabilité de NIOUDEME étant limitée aux mesures de sécurité mises en œuvre par ses propres services.
                </p>

                <h2 class="lightblue-text space-bottom"><i class="fa fa-chevron-right"></i> 4. Transfert et partage de données personnelles</h2>

                <h3>4.1. Transferts commerciaux</h3>

                <p>
                    Dans l'hypothèse d'une vente, fusion, réorganisation, dissolution ou autre évènement similaire, les données personnelles peuvent faire partie des données transférées. Dans ces cas de figure, vous reconnaissez et acceptez que n'importe quel repreneur, successeur ou acquéreur de NIOUDEME (ou de ses actifs) continuera d'avoir le droit à l'utilisation de vos données personnelles et autres informations conformément à la présente Politique de protection des données.
                </p>

                <h3>4.2. Filiales, sociétés mères</h3>

                <p>
                    Nous pouvons être amenés à partager vos données personnelles avec nos sociétés mères et/ou filiales aux fins indiquées dans la présente Politique de protection des données. Auquel cas, nos sociétés mères et/ou filiales sont tenues de conserver ces données personnelles conformément aux termes de la présente Politique de protection des données.
                </p>

                <h3>4.3. Sous-traitants et prestataires de services</h3>

                <p>
                    Nous pouvons être amenés à partager des informations, y compris des données personnelles, avec nos prestataires de services et sous-traitants qui exercent au nom de NIOUDEME certaines fonctions liées à nos activités. Ces sociétés comprennent les fournisseurs de services de base de données, les fournisseurs de services de sauvegarde et de récupération, les fournisseurs de services d'e-mail et autres.
                </p>

                <h3>4.4. Organisateurs</h3>

                <p>
                    Nous fournissons les données personnelles saisies par le participant sur un évènement ou expérience donné aux Organisateurs dudit évènement ou expérience. Dans certaines situations, un Organisateur peut désigner un tiers ("Organisateur Tiers") pour créer une page de l'évènement ou expérience. A titre d'exemple, une salle de spectacle peut désigner un promoteur d'évènement pour créer un évènement organisé dans la salle de spectacle en utilisant son compte NIOUDEME. Dans ces cas de figure, nous pouvons fournir vos données personnelles aux Organisateurs Tiers pour le compte des Organisateurs. Nous ne sommes nullement responsables des actes de ces Organisateurs, ou de ces Organisateurs Tiers vis-à-vis de vos données personnelles. Nous vous recommandons d'examiner les politiques de protection des données en vigueur de ces Organisateurs, ou le cas échéant - lorsque connus - des Organisateurs Tiers avant de leur transmettre vos données personnelles dans le cadre d'un évènement ou expérience connexe.
                </p>

                <h3>4.5. Gmail, Facebook et autres connexions tierces</h3>

                <p>
                    Vous pouvez vous connecter à votre compte NIOUDEME par le biais de vos comptes sur des services tiers tels que Gmail ou Facebook. Dans ce cas, nous pouvons recueillir et utiliser les informations relatives à votre compte auprès de ces services tiers conformément à la présente Politique de protection des données. A titre d'exemple, si vous vous connectez avec Gmail, nous conservons vos nom, prénom, email et photo de profil et nous les utilisons pour vous connecter et vous fournir certaines fonctionnalités sur nos services.
                </p>

                <h3>4.6. Exigences juridiques et légales</h3>

                <p>
                    Nous pouvons être contraints de divulguer vos données personnelles pour se conformer à une obligation juridique ou légale, pour prévenir ou enquêter sur de potentiels actes répréhensibles, pour répondre à une citation à comparaître ou une demande d'application de la loi, à un organisme gouvernemental ou à un tribunal.
                </p>

                <h2 class="lightblue-text space-bottom"><i class="fa fa-chevron-right"></i> 5. Conservation des données personnelles</h2>

                <p>
                    Nous pouvons conserver vos données personnelles aussi longtemps que vous utilisez nos services. Vous pouvez fermer votre compte en nous contactant à l'adresse&nbsp;<a href="mailto:contact@NIOUDEME.sn">contact@NIOUDEME.sn</a>.
                </p>

                <p>
                    A noter qu'en dépit de la fermeture de votre compte, nous pouvons conserver vos données dans un délai supplémentaire pour des raisons légales, fiscales et/ou réglementaires.
                </p>

                <p>
                    En outre, nous prenons des mesures que nous pensons être raisonnables pour protéger vos données personnelles contre toute perte, mauvaise utilisation, altération, accès non autorisé, divulgation accidentelle. Toutefois, comme aucun mécanisme n'offre une sécurité maximale, une part de risque est toujours présente lorsque l'on utilise Internet pour transmettre des renseignements personnels.
                </p>

                <h2 class="lightblue-text space-bottom"><i class="fa fa-chevron-right"></i> 6. Accès, modification ou suppression de vos données personnelles</h2>

                <p>
                    Vous pouvez exercer votre droit d'accès, de correction, mise à jour ou suppression de vos données personnelles en nous contactant à l'adresse&nbsp;<a href="mailto:contact@NIOUDEME.sn">contact@NIOUDEME.sn</a>.
                </p>

                <p>
                    A la demande du Participant, NIOUDEME est ainsi autorisé à supprimer les services ou à rendre anonyme les données personnelles du Participant, même si cela entraîne la suppression de la disponibilité desdites données envers l'Organisateur. Toutefois si ces données personnelles ont déjà été transmises à l'Organisateur avant que NIOUDEME ne reçoive la demande du Participant ou ne prenne les mesures nécessaires à la suppression ou l'anonymisation des données, il est possible que lesdites données subsistent toujours dans la base de données de l'Organisateur.
                </p>

                <p>
                    Par ailleurs, nous prendrons en compte et répondrons à toute demande ou exigence conformément à la législation en vigueur.
                </p>

                <h2 class="lightblue-text space-bottom"><i class="fa fa-chevron-right"></i> 7. Désabonnement de communications</h2>

                <h3>7.1. Communications Organisateur</h3>

                <p>
                    Les Organisateurs peuvent utiliser nos outils de messagerie pour communiquer avec leur base d'abonnés incluant les Participants à leurs évènements. Bien que ces communications soient émises au travers de nos outils, NIOUDEME n'est nullement responsable du contenu et ne détermine point les bénéficiaires desdites communications. Les Organisateurs sont en outre tenus de respecter les réglementations en vigueur lors de l'utilisation de nos outils de messagerie.
                </p>

                <p>
                    NIOUDEME fournit un lien de désabonnement dans chacun de ces emails afin de permettre aux destinataires de se désabonner des communications d'un Organisateur donné.
                </p>

                <h3>7.2. Communications Marketing NIOUDEME</h3>

                <p>
                    Nous pouvons vous adresser des communications marketing ou publicitaires sur des produits, services ou évènements que nous jugeons susceptibles de vous intéresser.
                </p>

                <p>
                    Nous fournissons un lien de désabonnement dans chacune de nos communications afin de vous permettre de vous désabonner desdites communications.
                </p>

                <h3>7.3. Délai de réponse et conservation</h3>

                <p>
                    Pour traiter votre demande de désabonnement, nous disposons d'un délai de 7 jours. A l'issu de ce désabonnement, nous continuons de conserver vos données personnelles conformément à la politique de protection des données en vigueur mais nous ne les utilisons plus pour vous contacter.
                </p>

                <p>
                    Si vous avez un compte NIOUDEME, nous conserverons généralement vos Données personnelles pendant une période de 90 jours après que vous avez demandé la fermeture de votre compte ou s'il est resté inactif pendant 6 ans.
                </p>

                <p>
                    Toutefois les Organisateurs ayant reçu vos données personnelles conformément à la présente politique de protection des données peuvent encore utiliser ces données pour vous contacter. Toutefois, ils ne pourront plus utiliser notre système pour le faire.
                </p>

                <h2 class="lightblue-text space-bottom"><i class="fa fa-chevron-right"></i> 8. Exclusions</h2>

                <h3>8.1. Liens Tiers</h3>

                <p>
                    La présente Politique de protection des données s'applique uniquement à nos services. Il est possible que nos services puissent contenir des liens vers d'autres sites tiers non exploités ou contrôlés par nous. Nous n'avalisons pas et ne passons pas en revue ces sites tiers. Aussi suggérons-nous que vous contactiez ces sites directement pour en savoir davantage sur leur politique de protection des données.
                </p>

                <h3>8.2. Données personnelles fournies à autrui</h3>

                <p>
                    La présente Politique de protection des données ne s'applique point aux données personnelles que vous pourriez fournir à tout autre utilisateur ou visiteur à travers nos services ou par tout autre moyen incluant par exemple des informations personnelles postées sur des espaces publics ou sur les pages d'un évènement.
                </p>

                <h3>8.3. Enfants</h3>

                <p>
                    Nous ne recueillons pas volontairement les données personnelles auprès d'enfants d'âge inférieur à 13 ans. Si vous avez moins de 13 ans, veuillez ne pas soumettre de données personnelles via nos services. Nous recommandons vivement les parents et responsables légaux des enfants à veiller à l'application de notre politique de protection des données et plus largement à surveiller l'utilisation d'internet de leur enfant.
                </p>

                <p>
                    Si vous pensez qu'un enfant de moins de 13 ans pourrait nous avoir fournir des informations personnelles, veuillez nous contacter et nous prendrons les mesures nécessaires pour la suppression desdites informations.
                </p>

