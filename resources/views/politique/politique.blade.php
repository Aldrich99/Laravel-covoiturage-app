                <h2 class="lightblue-text space-bottom"><i class="fa fa-chevron-right"></i> 1. Les données personnelles que nous collectons</h2>

                <p>
                    Les données personnelles que nous collectons sont recueillies au travers de formulaires et grâce à l'interactivité établie entre vous et notre site Web. Nous utilisons également, comme indiqué dans les sections suivantes, des fichiers témoins et/ou journaux pour réunir des informations vous concernant. Les données recueillies peuvent être pour notre propre compte et/ou pour le compte d'un Organisateur.
                </p>

                <p>
                    Dans le cadre de leurs relations contractuelles, les parties s'engagent à respecter la réglementation en vigueur applicable au traitement de données à caractère personnel et, en particulier, le règlement (UE) 2016/679 du Parlement européen et du Conseil du 27 avril 2016 applicable à compter du 25 mai 2018 (ci- après, « le règlement européen sur la protection des données »).
                </p>

                <h3>1.1. Utilisateurs</h3>

                <p>
                    Pour tous les Utilisateurs, nous collectons les données personnelles que vous nous fournissez volontairement pour accéder à nos services, pour transmettre vos questions ou pour répondre à nos enquêtes. Ces données comprennent ainsi votre nom, prénom, adresse électronique.
                </p>

                <p>
                    Nous recueillons automatiquement certaines informations par le biais de fichiers journaux (log files) et de fichiers témoins (cookies). Le recours à de tels fichiers nous permet de garder le site sécurisé et garantir une utilisation plus stable de la plateforme. Ces données recueillies automatiquement comprennent les informations suivantes : un identificateur unique associé à votre dispositif d'accès et/ou votre navigateur [incluant par exemple votre adresse de protocole internet IP], des statistiques sur vos activités sur les services [incluant par exemples les pages visitées sur le site, la date et heure de connexion], des informations sur la manière dont vous avez accédé aux services.
                </p>

                <h3>1.2. Organisateurs</h3>

                <p>
                    Nous collectons des données personnelles additionnelles auprès des Organisateurs.
                </p>

                <p>Nous recueillons ainsi auprès de l'organisateur&nbsp;:</p>

                <ul>
                    <li>le nom ou la raison sociale,</li>
                    <li>les renseignements d'ordre financier (coordonnées bancaires ou mobile money) nécessaires pour faciliter les virements de fonds,</li>
                    <li>l'adresse de facturation,</li>
                    <li>le numéro de TVA intracommunautaire le cas échéant,</li>
                    <li>le statut juridique,</li>
                    <li>l'adresse de messagerie</li>
                </ul>

                <p>
                    Il est également rappelé que NIOUDEME se réserve la possibilité de solliciter toute autre information relative à l'Organisateur notamment dans le cas où il aurait un doute sur la tenue de l'Evènement ou sur la moralité et la bonne foi de l'Organisateur.
                </p>

                <h3>1.3. Participants</h3>

                <p>
                    Les données à caractère personnel récoltées auprès des participants et traitées sont&nbsp;:
                </p>

                <ul>
                    <li>Par défaut, le nom, prénom, l'adresse de messagerie et le numéro de téléphone,</li>
                    <li>Selon les besoins de l'événement&nbsp;: la société, la date de naissance, l'adresse postale, une photo identifiant la personne,</li>
                    <li>A fin de traçabilité, l'adresse IP d'achat est également conservée,</li>
                    <li>La référence de paiement auprès de notre prestataire de paiement bancaire ou mobile money.</li>
                </ul>

                <p>En outre, les Organisateurs peuvent configurer les pages d'inscriptions à un évènement afin de collecter les informations des Participants dans le cadre de leur inscription à cet évènement.</p>

                <p>Dans l'hypothèse où l'Organisateur souhaiterait collecter d'autres données pour d'autres finalités, il conviendra qu'il indique aux participants la nature de la donnée et la finalité de la collecte.</p>

                <h2 class="lightblue-text space-bottom"><i class="fa fa-chevron-right"></i> 2. Cas d'utilisation des données personnelles que nous collectons et exercice des droits des personnes</h2>

                <h3>2.1. Accès et traitement</h3>

                <p>
                    Si vous nous fournissez des données personnelles dans le but d'accéder ou d'utiliser nos services ou fonctionnalités, nous utiliserons lesdites données personnelles pour vous permettre l'accès et l'utilisation des services ou fonctionnalités et pour analyser votre utilisation de ces services ou fonctionnalités.
                </p>

                <p>NIOUDEME est autorisé à traiter pour le compte de l'Organisateur les données à caractère personnel nécessaires pour fournir le ou les services suivants&nbsp;:</p>

                <ul>
                    <li>Vente de Billets par NIOUDEME aux Participants pour le compte de l'Organisateur
                    en application des présentes,</li>
                    <li>Mise à disposition de matériel permettant de contrôler l'accès des Participants
                    munis d'un Billet à l'Evènement de l'Organisateur,</li>
                    <li>Diffusion d'informations et de modifications sur le déroulement de l'événement.</li>
                </ul>

                <p>
                    La nature des opérations réalisées sur les données est&nbsp;:
                </p>

                <ul>
                    <li>La collecte et la restitution à l'identique au bénéfice de l'organisateur,</li>
                    <li>La conservation jusqu'à expiration au sein des systèmes NIOUDEME,</li>
                    <li>L'agrégation pour des statistiques de ventes et d'accès à l'événement.</li>
                </ul>

                <p>Les finalités du traitement sont&nbsp;:</p>

                <ul>
                    <li>L'identification du Participant,</li>
                    <li>La vérification que le Participant est autorisé à participer à l'événement,</li>
                    <li>La vérification des conditions particulières à l'une ou l'autre des catégories de tarif
                    de l'événement,</li>
                    <li>La diffusion d'informations pratiques sur le déroulement de l'événement,</li>
                    <li>La conservation de la preuve de la transaction intervenue entre NIOUDEME, le
                    Participant et l'Organisateur,</li>
                    <li>Pour rappel, dans l'hypothèse où l'Organisateur souhaiterait collecter d'autres
                    données pour d'autres finalités, il conviendra qu'il indique aux participants la
                    nature de la donnée et la finalité de la collecte.</li>
                </ul>

                <h3>2.2. Raisons commerciales internes</h3>

                <p>Les finalités commerciales internes nous conduisant à utiliser vos données comprennent :</p>

                <ul>
                    <li>l'amélioration de contenu et de fonctionnalités,</li>
                    <li>une meilleure compréhension des besoins et usages utilisateurs,</li>
                    <li>l'amélioration des services que nous proposons,</li>
                    <li>l'identification et la protection contre des actes fautifs,</li>
                    <li>le respect de nos Conditions Générales d'Utilisation et de Vente,</li>
                    <li>La gestion de votre compte, la fourniture d'un service client et la gestion de nos
                    services et activités.</li>
                </ul>

                <h3>2.3. Nos communications marketing</h3>

                <p>Nous pouvons utiliser vos Données personnelles pour vous contacter dans le cadre de nos campagnes marketing et publicitaires comprenant sans s'y limiter l'information sur nos Services ou les évènements ou les expériences qui peuvent vous intéresser, l'élaboration et la fourniture de matériel marketing ou promotionnel, l'affichage de contenus publicitaires qui peuvent selon nous vous intéresser.</p>

                <h3>2.4. Raison spécifique</h3>

                <p>
                    Nous pouvons utiliser vos données personnelles afin de répondre à l'objectif spécifique pour lequel vous nous les avez fournies. A titre d'exemple, nous répondrons à votre question ou problématique depuis l'adresse de contact que vous nous avez fournie.
                </p>

                <h3>2.5. Exercice des droits des personnes</h3>

                <p>
                    Dans la mesure du possible, NIOUDEME aidera l'Organisateur à s'acquitter de son obligation de donner suite aux demandes d'exercice des droits des personnes concernées&nbsp;: droit d'accès, de rectification, d'effacement et d'opposition, droit à la limitation du traitement, droit à la portabilité des données, droit de ne pas faire l'objet d'une décision individuelle automatisée (y compris le profilage).
                </p>

                <p>
                    L'Organisateur mandatera le cas échéant NIOUDEME pour répondre dans les délais légaux aux demandes des personnes concernées par le traitement en cas d'exercice de leurs droits, s'agissant des données faisant l'objet de la sous-traitance prévue au présent contrat.
                </p>

